package Basics;

public class ArithmeticOperation {

	public static void main(String[] args) {
		int a=5;
		int b=2;
		int c=a+b;
		System.out.println("Addition "+c);
		
		int x=5;
		int y=2;
		int z=x-y;
		System.out.println("Substraction "+z);
		
		int p=5;
		int q=2;
		int r=p*q;
		System.out.println("Multiplication "+r);
		
		int m=10;
		int n=2;
		int o=m/n;
		System.out.println("Division "+o);
		
	}

}
