package Basics;

public class SwitchStatement {

	public static void main(String[] args) {
		int number=4;
		switch(number)
		{
			case 1: System.out.println("Sunday");
			break;
		
			case 2: System.out.println("Monday");
			break;
			
			case 3: System.out.println("Tuesday");
			break;
		
			case 4: System.out.println("Wednesday");
			break;
		}
	}
}
