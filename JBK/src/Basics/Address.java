package Basics;

public class Address {
	int id;
	String addressline1;
	String addressline2;
	String landmark;
	String city;
	String pin;
	
	
	public Address(int id, String addressline1, String addressline2, String landmark, String city, String pin) {
		super();
		this.id = id;
		this.addressline1 = addressline1;
		this.addressline2 = addressline2;
		this.landmark = landmark;
		this.city = city;
		this.pin = pin;
		
		
	}


	
}
