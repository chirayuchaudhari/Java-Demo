package Array;

public class Array4 {

	public static void main(String[] args) {
		int a[]= {11,12,13,14,15};
		String s[]= {"Chirayu","Bhuvneshwar","Chaudhari"};
		
		System.out.println("Size of Int Array= "+a.length);
		System.out.println("Size of String Array= "+s.length);
		
		System.out.println("Element on 0th index= "+a[0]);
		System.out.println("String on 1st index= "+s[1]);
		}

}
